#include "D_Main.h"
#include "D_I2C.h"
#include "D_SIO.h"
#include "D_EIO.h"
//--------------------------------------------------------------------------------
// Program Name : 回り込みだけ.C
//--------------------------------------------------------------------------------
void user_main(void)
{
  double IR_motermax = 70;  	// 回り込む際のモータの最大出力
  double IR_moterValue;  	// 回り込む際のモータの最大出力結果
  double IR_frontValue = 10;  	// 前とみなす、角度の範囲（IR）
  double IR_edge = 0;  	// IRの角度
  double IR_alpha = 45;  	// 回り込む際のボールに対して何度ずらして動くか
  double Gyro_moter_max = 30;  	// ジャイロのモータの最大出力結果
  double Gyro_moter_min = 10;  	// ジャイロのモータの最大出力結果
  double Gyro_frontValue = 1;  	// 前とみなす、角度の範囲（Gyro）
  double Gyro_edge = 0;  	// ジャイロの角度
  double xx = 0;  	// 最終のモータ出力（X座標）
  double yy = 0;  	// 最終のモータ出力（Y座標）
  while (TRUE) {
    if (get_Uss(USS1) < 300) {
      xx = - IR_motermax;  	// 回り込むには、、（X座標）
      yy = IR_motermax;  	// 回り込むには、、（Y座標）
    } else if (gAD[CN2] < 500) {
      set_Led(0, LED_OFF);
      IR_edge = gAD[CN3] * 1.06 + 405;  	// ４５度左にずらす
      IR_edge = IR_edge * 3.14 / 180;  	// ラジアンに直す
      xx = cos(IR_edge) * IR_motermax;  	// ボールのほうに行くには、、（X座標）
      yy = sin(IR_edge) * IR_motermax;  	// ボールのほうに行くには、、（Y座標）
    } else if (gAD[CN3] * 1.06 < IR_frontValue * 2  &&  gAD[CN3] * 1.06 > 360 - IR_frontValue * 2) {
      set_Led(0, LED_OFF);
      IR_edge = gAD[CN3] * 1.06 + 405;  	// ４５度左にずらす
      IR_edge = IR_edge * 3.14 / 180;  	// ラジアンに直す
      xx = cos(IR_edge) * IR_motermax;  	// ボールのほうに行くには、、（X座標）
      yy = sin(IR_edge) * IR_motermax;  	// ボールのほうに行くには、、（Y座標）
    } else {
      set_Led(0, LED_ON);
      IR_moterValue = IR_motermax;
      if (gAD[CN3] * 1.06 > IR_frontValue  &&  gAD[CN3] * 1.06 < 360 - IR_frontValue) {
        if (gAD[CN3] * 0.98 < 180) {
          IR_edge = gAD[CN3] * 1.06 + 405 + IR_alpha;  	// ４５度＋IR＿alphaだけずらす
          IR_edge = IR_edge * 3.14 / 180;  	// ラジアンに直す
        } else {
          IR_edge = gAD[CN3] * 1.06 + 405 - IR_alpha;  	// ４５度-IR＿alphaだけずらす
          IR_edge = IR_edge * 3.14 / 180;  	// ラジアンに直す
        }
        xx = cos(IR_edge) * IR_moterValue;  	// 回り込むには、、（X座標）
        yy = sin(IR_edge) * IR_moterValue;  	// 回り込むには、、（Y座標）
      } else {
        xx = IR_motermax;  	// 直進のためには、、（X座標）
        yy = IR_motermax;  	// 直進のためには、、（Y座標）
      }
    }
    Gyro_edge = gAD[CN1];
    if (Gyro_edge > 50  &&  Gyro_edge < 310) {
      xx = 0;
      yy = 0;
    }
    if (Gyro_edge < Gyro_frontValue) {
      gV[VAR_G] = 0;
    } else if (Gyro_edge > 360 - Gyro_frontValue) {
      gV[VAR_G] = 0;
    } else if (Gyro_edge < 180) {
      gV[VAR_G] = ( (Gyro_moter_min - Gyro_moter_max) / 32400 ) * Gyro_edge * Gyro_edge;
      gV[VAR_G] = gV[VAR_G] - (Gyro_moter_min - Gyro_moter_max) / 90 * Gyro_edge + Gyro_moter_min;
    } else {
      gV[VAR_G] = ( (Gyro_moter_min - Gyro_moter_max) / 32400 ) * Gyro_edge * Gyro_edge;
      gV[VAR_G] = gV[VAR_G] - (Gyro_moter_min - Gyro_moter_max) / 90 * Gyro_edge + Gyro_moter_min;
      gV[VAR_G] = - gV[VAR_G];
    }
    gV[VAR_A] =  - xx + gV[VAR_G];  	// 代入
    gV[VAR_B] = yy + gV[VAR_G];  	// 代入
    gV[VAR_C] =  - yy + gV[VAR_G];  	// 代入
    gV[VAR_D] =  xx + gV[VAR_G];  	// 代入
    gPwm[0] = gV[VAR_A] < 0 ? (gV[VAR_A] * -1) | 0x80 : gV[VAR_A];
    gPwm[1] = gV[VAR_B] < 0 ? (gV[VAR_B] * -1) | 0x80 : gV[VAR_B];
    gPwm[2] = gV[VAR_C] < 0 ? (gV[VAR_C] * -1) | 0x80 : gV[VAR_C];
    gPwm[4] = gV[VAR_D] < 0 ? (gV[VAR_D] * -1) | 0x80 : gV[VAR_D];
    pwm_out();
  }
}
