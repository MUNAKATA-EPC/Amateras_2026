void user_main(void)
{
  while (TRUE) {
    gPwm[0] = 0x00 | 0x80;
    gPwm[1] = 0x64 | 0x80;
    gPwm[2] = 0x00;
    gPwm[3] = 0x64;
    pwm_out();
    set_Led(1, LED_ON);
    wait_ms(1000);
    gPwm[0] = 0x64;
    gPwm[1] = 0x00;
    gPwm[2] = 0x64 | 0x80;
    gPwm[3] = 0x00 | 0x80;
    pwm_out();
    set_Led(1, LED_OFF);
    wait_ms(1000);
  }
}
