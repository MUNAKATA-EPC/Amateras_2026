void user_main(void)
{
  while (TRUE) {
    if (judge_bno(0, 100, 80)) {
      gPwm[0] = 0x14;
      gPwm[1] = 0x14;
      gPwm[2] = 0x14;
      gPwm[3] = 0x14;
      pwm_out();
    } else if (judge_bno(0, 260, 80)) {
      gPwm[0] = 0x14 | 0x80;
      gPwm[1] = 0x14 | 0x80;
      gPwm[2] = 0x14 | 0x80;
      gPwm[3] = 0x14 | 0x80;
      pwm_out();
    } else {
      if (gAD[CN1] < 30) {
        gPwm[0] = 0x14 | 0x80;
        gPwm[1] = 0x14;
        gPwm[2] = 0x14 | 0x80;
        gPwm[3] = 0x14;
        pwm_out();
      } else {
        gPwm[0] = 0x00 | 0x80;
        gPwm[1] = 0x00 | 0x80;
        gPwm[2] = 0x00 | 0x80;
        gPwm[3] = 0x00 | 0x80;
        pwm_out();
      }
    }
  }
}
